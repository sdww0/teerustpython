#![forbid(unsafe_code)]

use unic::normal::StrNormalForm;
use unic::ucd::normal::compose;

fn main() {
    assert_eq!(compose('A', '\u{30a}'), Some('Å'));

    let s = "ÅΩ";
    let c = s.nfc().collect::<String>();
    assert_eq!(c, "ÅΩ");
}
